public class StringExercise {
    public static void main(String[] args) {
        String msg = "We don't need no education\nWe don't need no thought control\n\n";

        msg += "Check \"this\" out!, \"s inside of \"s!\n\n";

        msg += "In windows, the main drive is usually C:\\\n\n";

        msg += "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!\n\n";
        System.out.println(msg);
    }
}
