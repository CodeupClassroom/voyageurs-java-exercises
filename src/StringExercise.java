public class StringExercise {
    public static void main(String[] args) {
        String msg = "We don't need no education\nWe don't need no thought control";
        System.out.println(msg);
        msg = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(msg);
        msg = "In windows, the main drive is usually C:\\";
        System.out.println(msg);
        msg = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(msg);

    }
}
